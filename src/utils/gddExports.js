// ===== FULL GDD MARKDOWN =====

export function exportFullGDDMarkdown(project, gdd, storyStudio) {
  let md = `# ${project.title} — Game Design Document\n\n`
  md += `*Generated by HQ COMMAND on ${new Date().toLocaleDateString('en-GB')}*\n\n---\n\n`

  const textSections = [
    { key: 'vision',         emoji: '🎯', title: 'Vision Statement' },
    { key: 'coreLoop',       emoji: '🔄', title: 'Core Loop' },
    { key: 'story',          emoji: '📖', title: 'Story & Setting' },
    { key: 'artDirection',   emoji: '🎨', title: 'Art Direction' },
    { key: 'audioDirection', emoji: '🔊', title: 'Audio Direction' },
    { key: 'platform',       emoji: '📱', title: 'Platform & Technical' },
    { key: 'audience',       emoji: '👥', title: 'Target Audience' },
    { key: 'scope',          emoji: '📊', title: 'Scope & Constraints' },
  ]

  textSections.forEach(({ key, emoji, title }) => {
    const content = gdd?.sections?.[key]?.content
    if (!content) return
    md += `## ${emoji} ${title}\n\n${htmlToMarkdown(content)}\n\n---\n\n`
  })

  const mechanics = gdd?.sections?.mechanics || []
  if (mechanics.length) {
    md += `## ⚙️ Mechanics\n\n`
    mechanics.forEach(m => {
      md += `### ${m.name} \`${m.status}\`\n${htmlToMarkdown(m.description)}\n\n`
    })
    md += '---\n\n'
  }

  downloadFile(`${project.title}_GDD.md`, md)
}
