// ===== HELPERS =====

export function downloadFile(filename, content, mimeType = 'text/plain') {
  const blob = new Blob([content], { type: mimeType })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  a.click()
  URL.revokeObjectURL(url)
}

function stripHtml(html) {
  return (html || '').replace(/<[^>]*>/g, '').replace(/&nbsp;/g, ' ').replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>').trim()
}

export function htmlToMarkdown(html) {
  return (html || '')
    .replace(/<h[1-6][^>]*>(.*?)<\/h[1-6]>/gi, '## $1')
    .replace(/<strong>(.*?)<\/strong>/gi, '**$1**')
    .replace(/<em>(.*?)<\/em>/gi, '_$1_')
    .replace(/<u>(.*?)<\/u>/gi, '_$1_')
    .replace(/<li>(.*?)<\/li>/gi, '- $1')
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<hr\s*\/?>/gi, '\n---\n')
    .replace(/<[^>]*>/g, '')
    .replace(/&nbsp;/g, ' ')
    .trim()
}

// ===== DIALOGUE EXPORTS =====

export function exportDialogueYarn(scenes, projectTitle) {
  let yarn = `title: ${projectTitle || 'Dialogue'}\ntags:\n---\n`

  scenes.forEach(scene => {
    yarn += `\n// Scene: ${scene.title}\n`
    yarn += `=== ${scene.title.replace(/\s+/g, '_')} ===\n`
    if (scene.location) yarn += `// Location: ${scene.location}\n`
    scene.lines?.forEach(line => {
      if (line.type === 'action') yarn += `// ${line.content}\n`
      else if (line.type === 'dialogue') yarn += `${line.speaker}: ${line.content}\n`
      else if (line.type === 'direction') yarn += `// (${line.content})\n`
    })
    yarn += `===\n`
  })

  yarn += `\n===`
  downloadFile(`${projectTitle}_dialogue.yarn`, yarn)
}

export function exportDialogueInk(scenes, projectTitle) {
  let ink = `// ${projectTitle} — Dialogue Export (Ink format)\n// Generated by HQ COMMAND\n\n`

  scenes.forEach(scene => {
    ink += `=== ${scene.title.replace(/\s+/g, '_').toLowerCase()} ===\n`
    if (scene.location) ink += `// Location: ${scene.location}\n`
    scene.lines?.forEach(line => {
      if (line.type === 'action') ink += `~ // ${line.content}\n`
      else if (line.type === 'dialogue') ink += `${line.speaker}: ${line.content}\n`
      else if (line.type === 'direction') ink += `// (${line.content})\n`
    })
    ink += `-> DONE\n\n`
  })

  downloadFile(`${projectTitle}_dialogue.ink`, ink)
}

export function exportDialogueCSV(scenes, projectTitle) {
  const rows = [['Scene', 'Location', 'Type', 'Speaker', 'Line', 'Node_ID']]

  scenes.forEach(scene => {
    scene.lines?.forEach((line, idx) => {
      rows.push([
        `"${scene.title}"`,
        `"${scene.location || ''}"`,
        `"${line.type}"`,
        `"${line.speaker || ''}"`,
        `"${(line.content || '').replace(/"/g, '""')}"`,
        `"${scene.title.replace(/\s+/g, '_')}_${idx}"`,
      ])
    })
  })

  downloadFile(`${projectTitle}_dialogue.csv`, rows.map(r => r.join(',')).join('\n'), 'text/csv')
}

export function exportDialogueFountain(scenes, projectTitle) {
  let fountain = `Title: ${projectTitle}\nCredit: Written by\nContact: Generated by HQ COMMAND\n\n`

  scenes.forEach(scene => {
    fountain += `\nINT. ${(scene.location || scene.title).toUpperCase()} - DAY\n\n`
    scene.lines?.forEach(line => {
      if (line.type === 'action') fountain += `${line.content}\n\n`
      else if (line.type === 'dialogue') fountain += `${line.speaker.toUpperCase()}\n${line.content}\n\n`
      else if (line.type === 'direction') fountain += `(${line.content})\n\n`
    })
  })

  downloadFile(`${projectTitle}_screenplay.fountain`, fountain)
}

export function exportDialoguePlainText(scenes, projectTitle) {
  let text = `${projectTitle.toUpperCase()}\nDIALOGUE SCRIPT\nGenerated by HQ COMMAND\n${'='.repeat(50)}\n\n`

  scenes.forEach(scene => {
    text += `SCENE: ${scene.title.toUpperCase()}\n`
    if (scene.location) text += `LOCATION: ${scene.location}\n`
    text += `${'-'.repeat(30)}\n\n`
    scene.lines?.forEach(line => {
      if (line.type === 'action') text += `${line.content}\n\n`
      else if (line.type === 'dialogue') text += `${line.speaker.toUpperCase()}\n  ${line.content}\n\n`
      else if (line.type === 'direction') text += `  (${line.content})\n\n`
    })
    text += '\n'
  })

  downloadFile(`${projectTitle}_script.txt`, text)
}

// ===== MECHANICS EXPORTS =====

export function exportMechanicsCSV(mechanics, projectTitle) {
  const rows = [['Name', 'Status', 'Description', 'Notes']]
  mechanics.forEach(m => {
    rows.push([
      `"${m.name}"`,
      `"${m.status}"`,
      `"${stripHtml(m.description).replace(/"/g, '""')}"`,
      `"${(m.notes || '').replace(/"/g, '""')}"`,
    ])
  })
  downloadFile(`${projectTitle}_mechanics.csv`, rows.map(r => r.join(',')).join('\n'), 'text/csv')
}

export function exportMechanicsMarkdown(mechanics, projectTitle) {
  let md = `# ${projectTitle} — Game Mechanics\n\n`
  const byStatus = {}
  mechanics.forEach(m => {
    if (!byStatus[m.status]) byStatus[m.status] = []
    byStatus[m.status].push(m)
  })

  Object.entries(byStatus).forEach(([status, items]) => {
    md += `## ${status}\n\n`
    items.forEach(m => {
      md += `### ${m.name}\n`
      md += `${htmlToMarkdown(m.description)}\n\n`
      if (m.notes) md += `> ${m.notes}\n\n`
    })
  })

  downloadFile(`${projectTitle}_mechanics.md`, md)
}

export function exportMechanicsJira(mechanics, projectTitle) {
  const tickets = mechanics.map(m => ({
    summary: m.name,
    description: stripHtml(m.description),
    status: m.status === 'Implemented' ? 'Done' : m.status === 'In Development' ? 'In Progress' : 'To Do',
    labels: ['mechanic', projectTitle.toLowerCase().replace(/\s+/g, '-')],
    priority: m.status === 'Planned' ? 'Medium' : 'High',
  }))

  downloadFile(`${projectTitle}_jira_import.json`, JSON.stringify(tickets, null, 2), 'application/json')
}

// ===== CHARACTER EXPORTS =====

export function exportCharactersMarkdown(characters, projectTitle) {
  let md = `# ${projectTitle} — Character Bible\n\nGenerated by HQ COMMAND\n\n---\n\n`

  characters.forEach(char => {
    md += `## ${char.name}\n\n`
    md += `**Role:** ${char.role || 'Unknown'}`
    if (char.age) md += ` | **Age:** ${char.age}`
    md += ` | **Status:** ${char.status || 'Active'}\n\n`
    if (char.appearance) md += `### Appearance\n${htmlToMarkdown(char.appearance)}\n\n`
    if (char.personality) md += `### Personality\n${htmlToMarkdown(char.personality)}\n\n`
    if (char.backstory) md += `### Backstory\n${htmlToMarkdown(char.backstory)}\n\n`
    if (char.motivations) md += `### Motivations\n${char.motivations}\n\n`
    if (char.fears) md += `### Fears\n${char.fears}\n\n`
    md += '---\n\n'
  })

  downloadFile(`${projectTitle}_characters.md`, md)
}

