// ===== WORLD BUILDING EXPORTS =====

export function exportWorldMarkdown(world, projectTitle) {
  let md = `# ${projectTitle} — World Bible\n\nGenerated by HQ COMMAND\n\n---\n\n`

  const sections = [
    { key: 'locations', title: 'Locations' },
    { key: 'factions', title: 'Factions' },
    { key: 'lore', title: 'Lore' },
    { key: 'rules', title: 'World Rules' },
  ]

  sections.forEach(({ key, title }) => {
    const items = world[key] || []
    if (!items.length) return
    md += `## ${title}\n\n`
    items.forEach(item => {
      md += `### ${item.name}\n`
      if (item.tags?.length) md += `*Tags: ${item.tags.join(', ')}*\n\n`
      md += `${htmlToMarkdown(item.description)}\n\n---\n\n`
    })
  })

  downloadFile(`${projectTitle}_world_bible.md`, md)
}

// ===== STORY ARCS EXPORT =====

export function exportArcsMarkdown(arcs, projectTitle) {
  let md = `# ${projectTitle} — Story Arcs\n\n`
  arcs.forEach(arc => {
    md += `## ${arc.title}\n`
    md += `**Status:** ${arc.status}\n\n`
    if (arc.summary) md += `${htmlToMarkdown(arc.summary)}\n\n`
    if (arc.beats?.length) {
      md += `### Story Beats\n\n`
      arc.beats.forEach((beat, i) => {
        md += `${i + 1}. **${beat.title}**\n   ${beat.description || ''}\n\n`
      })
    }
    md += '---\n\n'
  })
  downloadFile(`${projectTitle}_story_arcs.md`, md)
}

